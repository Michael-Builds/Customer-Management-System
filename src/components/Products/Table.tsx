import React, { useState } from "react"
import styles from "./product.module.css"
import { RiDeleteBin6Line } from "react-icons/ri"
import { PiNotePencilDuotone } from "react-icons/pi"
import { MdKeyboardArrowRight } from "react-icons/md"
import { MdKeyboardArrowLeft } from "react-icons/md"
import { Checkbox } from "@mantine/core"
import Image from "next/image"

const itemsPerPage = 10

const Table = () => {
  const [currentPage, setCurrentPage] = useState(1)

  const totalPages = Math.ceil(people.length / itemsPerPage)

  const setPage = (page: any) => {
    setCurrentPage(page)
  }
  const currentData = people.slice(
    (currentPage - 1) * itemsPerPage,
    currentPage * itemsPerPage
  )

  return (
    <div className="">
      <div className="-mx-4 -my-2 overflow-x-auto sm:-mx-6 lg:-mx-8">
        <div className={`inline-block min-w-full align-middle sm:px-6 lg:px-8`}>
          <table
            className={`min-w-full divide-y divide-gray-300 border ${styles.guards}`}
          >
            <thead>
              <tr>
                <th className="pl-4">
                  <Checkbox size="16" />
                </th>
                <th
                  scope="col"
                  className="py-3.5 pl-4 pr-3 text-center text-sm font-semibold text-gray-900 sm:pl-0"
                >
                  Name
                </th>
                <th
                  scope="col"
                  className="px-3 py-3.5 text-left text-sm font-semibold text-gray-900"
                >
                  Title
                </th>
                <th
                  scope="col"
                  className="px-3 py-3.5 text-left text-sm font-semibold text-gray-900"
                >
                  Status
                </th>
                <th
                  scope="col"
                  className="px-3 py-3.5 text-left text-sm font-semibold text-gray-900"
                >
                  Role
                </th>
                <th
                  scope="col"
                  className="px-3 py-3.5 text-left text-sm font-semibold text-gray-900"
                >
                  Actions
                </th>
              </tr>
            </thead>
            <tbody className="divide-y divide-gray-200 bg-white">
              {currentData.map((person, index) => (
                <tr key={index} className={styles.details}>
                  <td className="pl-4">
                    <Checkbox size="16" />
                  </td>
                  <td className="whitespace-nowrap pl-4 pr-3 py-1.5 text-sm sm:pl-0">
                    <div className="flex items-center pl-4">
                      <div className="h-11 w-11 flex-shrink-0">
                        <Image
                          className="h-11 w-11 rounded-full"
                          src={person.image}
                          alt="user image"
                        />
                      </div>
                      <div className="ml-4">
                        <div className="font-medium text-gray-900">
                          {person.name}
                        </div>
                        <div className="mt-1 text-gray-500">{person.email}</div>
                      </div>
                    </div>
                  </td>
                  <td className="whitespace-nowrap px-3 py-1.5 text-sm text-gray-500">
                    <div className="text-gray-900">{person.title}</div>
                    <div className="mt-1 text-gray-500">
                      {person.department}
                    </div>
                  </td>
                  <td className="whitespace-nowrap px-3 py-1.5 text-sm text-gray-500">
                    <span className="inline-flex items-center rounded-md bg-green-50 px-2 py-1 text-xs font-medium text-green-700 ring-1 ring-inset ring-green-600/20">
                      Active
                    </span>
                  </td>
                  <td className="whitespace-nowrap px-3 py-1.5 text-sm text-gray-500">
                    {person.role}
                  </td>
                  <td
                    className={` ${styles.actions} relative whitespace-nowrap py-5 pl-3 pr-4  text-sm font-medium sm:pr-0`}
                  >
                    <RiDeleteBin6Line className="h-4 w-4 cursor-pointer text-red-500" />
                    <PiNotePencilDuotone className="h-4 w-4 cursor-pointer text-blue-500" />
                  </td>
                </tr>
              ))}
            </tbody>
          </table>

          {/* Pagination */}
          <div className={`px-4 py-4 flex justify-between ${styles.buttons}`}>
            <button
              onClick={() => currentPage > 1 && setPage(currentPage - 1)}
              className={`${styles.paginate} ${
                currentPage > 1 ? "" : styles.disabledButton
              }`}
              disabled={currentPage <= 1}
            >
              <MdKeyboardArrowLeft />
              <p className="pr-1 text-sm"> Previous</p>
            </button>
            <button
              onClick={() =>
                currentPage < totalPages && setPage(currentPage + 1)
              }
              className={`${styles.paginate}  ${
                currentPage < totalPages ? "" : styles.disabledButton
              }`}
              disabled={currentPage >= totalPages}
            >
              <p className="text-sm"> Next</p>
              <MdKeyboardArrowRight />
            </button>
          </div>
        </div>
      </div>
    </div>
  )
}

export default Table

